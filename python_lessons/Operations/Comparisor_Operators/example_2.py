"""
!= operator in Python is used to compare two values to see if \
they are not equal. It returns True if the values are different, and False if they are the same.

"""

# Define two variables
a = 10
b = 5

# Compare the variables using !=
if a != b:
    print("a and b are not equal")
else:
    print("a and b are equal")

# Another example with equal values
c = 7
d = 7

# Compare the variables using !=
if c != d:
    print("c and d are not equal")
else:
    print("c and d are equal")
