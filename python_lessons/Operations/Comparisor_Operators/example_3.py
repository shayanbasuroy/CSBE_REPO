"""
the > operator in Python is used to compare two values to see if the first value is greater than the second value. It returns True if the first value is greater, and False otherwise.


"""
# Define some variables
a = 10
b = 5

# Compare the variables using >
if a > b:
    print("a is greater than b")
else:
    print("a is not greater than b")

# Another example with different values
c = 7
d = 9

# Compare the variables using >
if c > d:
    print("c is greater than d")
else:
    print("c is not greater than d")
